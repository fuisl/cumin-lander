void updateManekiNeko(Adafruit_SSD1306 &display)
{
    unsigned long currentTime = millis();

    // Update animation frame if it's time
    if (currentTime - lastFrameTime > FRAME_DELAY)
    {
        animationFrame = (animationFrame + 1) % FRAME_COUNT;
        lastFrameTime = currentTime;
    }

    display.clearDisplay();

    // Draw the current frame directly from the PROGMEM data
    display.drawBitmap(45, 2, frames[animationFrame], 32, 32, SSD1306_WHITE);

    display.display();
}

